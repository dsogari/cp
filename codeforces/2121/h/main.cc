template <typename T> struct Number {
  T x;
  Number() { cin >> x; }
  Number(T a) : x(a) {}
  operator T &() { return x; }
  operator T() const { return x; }
};

using Int = Number<int>;

void solve(int t) {
  Int n;
  vector<array<Int, 2>> a(n);
  multiset<int> seen;
  vector<int> ans;
  for (auto [l, r] : a) {
    seen.insert(l);
    auto it = seen.upper_bound(r);
    if (it != seen.end()) {
      seen.erase(it);
    }
    ans.push_back(seen.size());
  }
  println(ans);
}

int main() {
  cin.tie(nullptr)->tie(nullptr)->sync_with_stdio(false);
  Int t;
  for (int i = 1; i <= t; i++) {
    solve(i);
  }
}
