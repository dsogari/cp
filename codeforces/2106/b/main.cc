template <typename T> struct Num {
  T x;
  Num() { cin >> x; }
  Num(T a) : x(a) {}
  operator T &() { return x; }
  operator T() const { return x; }
};
using Int = Num<int>;

void solve(int t) {
  Int n, x;
  vector<int> ans(n);
  for (int i = 0, j = 0; i < n - 1; i++, j++) {
    ans[i] = j == x ? ++j : j;
  }
  ans[n - 1] = min(n - 1, +x);
  println(ans);
}

int main() {
  cin.tie(nullptr)->tie(nullptr)->sync_with_stdio(false);
  Int t;
  for (int i = 1; i <= t; i++) {
    solve(i);
  }
}
