template <typename T> struct Num {
  T x;
  Num() { cin >> x; }
  Num(T a) : x(a) {}
  operator T &() { return x; }
  operator T() const { return x; }
};
using Int = Num<int>;

void solve(int t) {
  Int n, s;
  vector<Int> a(n);
  auto mn = *ranges::min_element(a);
  auto mx = *ranges::max_element(a);
  auto ans = mx - mn + min(abs(s - mn), abs(s - mx));
  println(ans);
}

