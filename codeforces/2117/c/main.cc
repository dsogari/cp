template <typename T> struct Num {
  T x;
  Num() { cin >> x; }
  Num(T a) : x(a) {}
  operator T &() { return x; }
  operator T() const { return x; }
};
using Int = Num<int>;

void solve(int t) {
  Int n;
  vector<Int> a(n);
  set<int> set1, set2;
  int ans = 0;
  for (int i = 0; i < n; i++) {
    set1.insert(a[i]);
    set2.erase(a[i]);
    if (set2.empty()) {
      swap(set1, set2);
      ans++;
    }
  }
  println(ans);
}

int main() {
  cin.tie(nullptr)->tie(nullptr)->sync_with_stdio(false);
  Int t;
  for (int i = 1; i <= t; i++) {
    solve(i);
  }
}
